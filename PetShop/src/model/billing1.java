    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        String loai = jComboBox1.getSelectedItem().toString();
        try {
            Connection con = ConnectionProvider.getConnection();
            Statement st = con.createStatement();
            if(loai.equals("All")){
                ResultSet rs = st.executeQuery("select pId,pName,Species,Price from Pet where Status = 1");
                jTable2.setModel(DbUtils.resultSetToTableModel(rs));
            }else{
                ResultSet rs = st.executeQuery("select pId,pName,Species,Price from Pet where Species='"+loai+"' and Status = 1");
                jTable2.setModel(DbUtils.resultSetToTableModel(rs));
            }
            jTable2.setVisible(false);
            jTable2.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

