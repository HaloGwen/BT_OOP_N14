                    doc.add(paragraph3);
                    Paragraph paragraph4 = new Paragraph("Species: "+Species+"\n");
                    doc.add(paragraph4);
                    Paragraph paragraph5 = new Paragraph("Price: "+Price+"\nPaid Amount: "+paid+"\nReturn Amount: "+retun+"\n\nThanks you for Visiting!\nPMS");
